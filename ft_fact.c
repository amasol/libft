#include <stdio.h>

int		ft_fact(long int i)
{
	long int k;
	long int res;

	k = 1;
	res = 1;
	while (k <= i)
	{
		res = res * k;
		k++;
	}
	return (res);
}

int main()
{
	printf("%d\n", ft_fact(20));
}