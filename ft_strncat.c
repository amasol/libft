#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t k;

	i = -1;
	k = ft_strlen(s1);
	while (s2[++i] && (i < n))
	{
		s1[k + i] = s2[i];
	}
	s1[k + i] = '\0';
	return (s1);
}
